export function getDepartmentContent(key) {
  const entry = departmentContent[key];
  if (!entry) {
    return null;
  }

  if (entry.aliasOf) {
    return departmentContent[entry.aliasOf] || null;
  }

  return entry;
}

export function getDepartmentKeyFromUsername(username) {
  const normalizedUsername = String(username || "").toLowerCase();
  const keys = Object.keys(departmentContent).sort((left, right) => right.length - left.length);

  return (
    keys.find(
      (key) =>
        normalizedUsername === key ||
        normalizedUsername.startsWith(key) ||
        normalizedUsername.includes(key)
    ) || null
  );
}
